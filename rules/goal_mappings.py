def get_goal_weights(goal: str) -> Dict[str, float]:
    """
    Получить веса типов программ для заданной цели.

    Args:
        goal: Цель атлета (lowercase)

    Returns:
        Словарь {тип_программы: вес}
    """
    goal_lower = goal.lower()
    return GOAL_TO_TYPES.get(goal_lower, GOAL_TO_TYPES['поддержание'])


def get_focus_types(focus_area: str) -> list:
    """
    Получить типы программ для фокусной области.

    Args:
        focus_area: Фокусная область (lowercase)

    Returns:
        Список типов программ
    """
    focus_lower = focus_area.lower()
    return FOCUS_TO_TYPES.get(focus_lower, [])


def normalize_goal(goal: str) -> str:
    """
    Нормализовать название цели (привести к lowercase).

    Args:
        goal: Цель атлета

    Returns:
        Нормализованное название цели
    """
    return goal.lower().strip()


def get_all_goals() -> list:
    """
    Получить список всех поддерживаемых целей.

    Returns:
        Список целей
    """
    return list(GOAL_TO_TYPES.keys())
